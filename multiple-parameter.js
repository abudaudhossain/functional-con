// add two number 
function addTwoNumbers(number1, number2) {
    return number1 + number2;
}

// multiplication two numbers  
function multiplyTwoNumbers(number1, number2) {
    return number1 * number2;
}

// difference two Numbers 
function diffeTwoNumber(number1, number2) {
    return number1 - number2;
}

var sum = addTwoNumbers(12, 34);
var multipply = multiplyTwoNumbers(12, 34);
var Defferent = diffeTwoNumber(12, 34);

console.log(sum);
console.log(multipply);
